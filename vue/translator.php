<?php

function Frenchtranslator($word){
	
    if($word=="view")
    {
        return "Consulter";
        
    } else if($word=="add")
    {
        return "Ajouter";
        
    }else if($word=="update")
    {
        return "Modifier";
        
    }else if($word=="delete")
    {
        return "Supprimer";
        
    }else if($word=="group")
    {
        return "Groupe";
        
    }else if($word=="building")
    {
        return "Bâtiment";
        
    }else if($word=="classroom")
    {
        return "Local";
        
    }else if($word=="customer")
    {
        return "Client";
        
    }else if($word=="program")
    {
        return "Programme";
        
    }else if($word=="qualification")
    {
        return "Compétence";
        
    }else if($word=="teacher")
    {
        return "Professeur";
        
    }
    else if($word=="user")
    {
        return "Utilisateur";
        
    }else if($word=="right")
    {
        return "Droit";
        
    }else if($word=="role")
    {
        return "Rôle";
        
    }
    
}
